            'group_by_field' => 'created_at',
            'group_by_period' => 'month',
            'chart_type' => 'bar',
        ];
        $chart1 = new LaravelChart($chart_options);

        return view('reportes.index', compact('caja', 'pago', 'pago_pedidos', 'clientes', 'chart1'));
